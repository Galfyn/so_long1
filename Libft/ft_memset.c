#include <stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	int				i;
	unsigned char	*dest;

	dest = (unsigned char *)b;
	i = 0;
	while (len != 0)
	{
		dest[i] = c;
		len--;
		i++;
	}
	return (b);
}
